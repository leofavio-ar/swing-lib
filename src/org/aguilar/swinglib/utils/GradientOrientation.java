/**
 *
 * @author Leo Aguilar
 */
public enum GradientOrientation {
    /** El valor que indica un gradiente de izquierda a derecha. */
    LEFT_TO_RIGHT,
    /** El valor que indica un gradiente de derecha a izquierda. */
    RIGHT_TO_LEFT,
    /** El valor que indica un gradiente de arriba hacia abajo. */
    TOP_TO_BOTTOM,
    /** El valor que indica un gradiente de abajo hacia arriba. */
    BOTTOM_TO_TOP
}